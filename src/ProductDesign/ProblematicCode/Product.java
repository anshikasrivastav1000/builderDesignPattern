package ProductDesign.ProblematicCode;

public class Product {
    private String name;
    private String desc;
    private int price;
    private String brand;
    private String category;
    private int discount;
  Product(String name, String desc,int price,String brand,String category, int discount){
    this.name = name;
    this.desc = desc;
    this.price = price;
    this.brand = brand;
    this.category = category;
    this.discount = discount;
  }

}
